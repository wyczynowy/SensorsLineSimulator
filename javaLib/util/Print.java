package util;

public class Print {
	public static void println(String string) { System.out.println(string); }
	public static void println(int integer) { System.out.println(integer); }
	public static void println(double number) { System.out.println(number); }
	
	public static void print(String string) { System.out.print(string); }
	public static void print(int integer) { System.out.print(integer); }
	public static void print(double number) { System.out.println(number); }
}
